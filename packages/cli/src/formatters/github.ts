function collectFilesChanged(scenes: Scene[]): Map<string, number> {
  const files = new Map<string, number>();
  for (const scene of scenes) {
    if (scene.type !== "tool-call") continue;
    if (scene.toolName === "Edit" || scene.toolName === "Write") {
      const fp = scene.input.file_path;
      if (fp) {
        const name = shortPath(fp);
        files.set(name, (files.get(name) || 0) + 1);
      }
    }
  }
  return files;
}

/** Show last 2 path segments for disambiguation (avoids basename collisions) */
function shortPath(fullPath: string): string {
  const parts = fullPath.split("/");
  if (parts.length <= 2) return parts.join("/");
  return parts.slice(-2).join("/");
}

function formatDuration(ms?: number): string | null {
  if (ms == null) return null;
  const sec = Math.round(ms / 1000);
  if (sec < 60) return `${sec}s`;
  const min = Math.floor(sec / 60);
  if (min >= 60) {
    const hr = Math.floor(min / 60);
    const rm = min % 60;
    return rm > 0 ? `${hr}h ${rm}m` : `${hr}h`;
  }
  return `${min} min`;
}

/** Visual width: CJK characters count as 2, others as 1 */
function visualWidth(s: string): number {
  let w = 0;
  for (const ch of s) {
    const code = ch.codePointAt(0) || 0;
    w += isCjk(code) ? 2 : 1;
  }
  return w;
}

function isCjk(code: number): boolean {
  return (
    (code >= 0x3000 && code <= 0x9fff) || // CJK Unified + Symbols + Kana
    (code >= 0xac00 && code <= 0xd7af) || // Hangul
    (code >= 0xf900 && code <= 0xfaff) || // CJK Compatibility
    (code >= 0xff00 && code <= 0xff60) || // Fullwidth forms
    (code >= 0x20000 && code <= 0x2fa1f) // CJK Extension B–F
  );
}

/** Truncate string to fit within maxWidth visual columns */
function condenseLine(s: string, maxWidth: number): string {
  const cleaned = s.replace(/\n/g, " ").replace(/\s+/g, " ").trim();
  if (visualWidth(cleaned) <= maxWidth) return cleaned;
  // Truncate character by character
  let w = 0;
  let i = 0;
  for (const ch of cleaned) {
    const cw = isCjk(ch.codePointAt(0) || 0) ? 2 : 1;
    if (w + cw > maxWidth - 1) break;
    w += cw;
    i += ch.length;
  }
  return `${cleaned.slice(0, i)}…`;
}

/** Wrap text into 1 or 2 lines, respecting visual width */
function wrapText(s: string, maxWidth: number): [string] | [string, string] {
  const cleaned = s.replace(/\n/g, " ").replace(/\s+/g, " ").trim();
  if (visualWidth(cleaned) <= maxWidth) return [cleaned];
  // Find break point at word/CJK boundary
  let w = 0;
  let lastSpace = -1;
  let i = 0;
  for (const ch of cleaned) {
    const cw = isCjk(ch.codePointAt(0) || 0) ? 2 : 1;
    if (w + cw > maxWidth) break;
    if (ch === " ") lastSpace = i;
    w += cw;
    i += ch.length;
  }
  const breakAt = lastSpace > i * 0.3 ? lastSpace : i;
  return [cleaned.slice(0, breakAt).trim(), condenseLine(cleaned.slice(breakAt).trim(), maxWidth)];
}

function baseName(p: string): string {
  return p.split("/").pop() || p;
}

function escMd(s: string): string {
  return s.replace(/[[\]()*_`\\]/g, "\\$&");
}

function escXml(s: string): string {
  return s
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&apos;");
}

/** Format a number with max 2 decimal places, stripping trailing zeros */
function n2(n: number): string {
  return Number(n.toFixed(2)).toString();
}
